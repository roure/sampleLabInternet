    @Test
    @Transactional
    void createPlayer() throws Exception {
        int databaseSizeBeforeCreate = playerRepository.findAll().size();
        // Create the Player
        restPlayerMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(player)))
            .andExpect(status().isCreated());

        // Validate the Player in the database
        List<Player> playerList = playerRepository.findAll();
        assertThat(playerList).hasSize(databaseSizeBeforeCreate + 1);
        Player testPlayer = playerList.get(playerList.size() - 1);
        assertThat(testPlayer.getName()).isEqualTo(DEFAULT_NAME);
        assertThat(testPlayer.getBirthDate()).isEqualTo(DEFAULT_BIRTH_DATE);
        assertThat(testPlayer.getHeight()).isEqualTo(DEFAULT_HEIGHT);
        assertThat(testPlayer.getWeight()).isEqualTo(DEFAULT_WEIGHT);
        assertThat(testPlayer.getBaskets()).isEqualTo(DEFAULT_BASKETS);
        assertThat(testPlayer.getAssists()).isEqualTo(DEFAULT_ASSISTS);
    }

